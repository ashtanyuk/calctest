public class Calc {
    public static int plus(int x, int y) {
        return x + y;
    }
    public static int minus(int x, int y) {
        return x - y;
    }
    public static int mul(int x, int y) {
        return x * y;
    }
    public static int div(int x, int y) {
        return x / y;
    }
}
